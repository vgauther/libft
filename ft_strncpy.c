#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, unsigned int n)
{
	unsigned int	i;
	int				t;

	i = 0;
	t = n;
	if (n == 0 && !(src))
	{
		dest[0] = '\0';
		return (dest);
	}
	while (src[i] && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
